package com.company.Polymorphism.Q4;

public class MyMath {
    // int
    public int getSum(int Num1, int Num2){
        return (Num1 + Num2);
    }
    // byte
    public int getSum(byte Num1, byte Num2){
        return (Num1 + Num2);
    }
    // float
    public float getSum(float Num1, float Num2){
        return (Num1 + Num2);
    }
    // int + byte
    public int getSum(int Num1, byte Num2){
        return (Num1 + Num2);
    }
    // int + float
    public float getSum(float Num1, int Num2){
        return (Num1 + Num2);
    }
    // byte + float
    public float getSum(byte Num1, float Num2){
        return (Num1 + Num2);
    }

}
